alunos = ['Adriana', 'João', 'Hiago', 'Thiago', 'Gael', 'Juliana', 'Adriana']
print(f' Lista de alunos: {alunos}')
# Método remover()
alunos.remove('Adriana')
print(f' Lista de alunos: {alunos}')
#Método pop
alunos.pop(2)
print(f' Lista de alunos: {alunos}')
# Função del
del alunos[3]
print(f' Lista de alunos:{alunos}')
# Método clear
alunos.clear()
print(f' Lista de alunos: {alunos}')

